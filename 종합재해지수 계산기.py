import math


def 사용자_입력(안내문):
    while True:
        try:
            입력값 = float(input(안내문))
            return 입력값
        except ValueError:
            print("올바른 숫자를 입력하세요.")


def 종합재해지수_계산(근로손실시간, 재해발생건수, 연근로시간):
    강도율 = (근로손실시간 / 연근로시간) * 1000
    도수율 = (재해발생건수 / 연근로시간) * 1000000
    종합재해지수 = math.sqrt(강도율 * 도수율)
    return 종합재해지수


def 메인():
    print("종합재해지수 계산기에 오신 것을 환영합니다.")


    근로손실시간 = 사용자_입력("근로 손실 시간을 입력하세요: ")
    재해발생건수 = 사용자_입력("재해 발생 건수를 입력하세요: ")
    연근로시간 = 사용자_입력("연 근로 시간을 입력하세요: ")


    종합재해지수 = 종합재해지수_계산(근로손실시간, 재해발생건수, 연근로시간)


    print("종합재해지수:", 종합재해지수)

if __name__ == "__main__":
    메인()
